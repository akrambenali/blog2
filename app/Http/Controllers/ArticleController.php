           'title'=> 'required|min:6|max:255',
           'sub_title' => 'required',
           'published_at' => 'required|date',
           'body' => 'required',
           'user_id' => 'required|integer',
       ]);

       $article = Article::create(request()->all()+ ['slug'=> \Str::slug(request('title'))]);
       return redirect()->route('pages.index');
